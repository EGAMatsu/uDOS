struct x3390_seek {
    uint16_t block;
    uint16_t cyl;
    uint16_t head;
    uint8_t record;
} __attribute__((aligned(8)));

int DevFDSCB_ReadDisk(struct css_device *dev, struct fs_fdscb *fdscb, void *buf, size_t n)
{
    struct x3390_seek seek_ptr;
    struct css_request *req;

    req = CssNewRequest(dev, 4);
    if(req == NULL) {
        KePanic("Out of memory");
    }

    req->ccws[0].cmd = X3390_CMD_SEEK;
    CSS_SET_ADDR(&req->ccws[0], &seek_ptr.block);
    req->ccws[0].flags = CSS_CCW_CC;
    req->ccws[0].length = 6;

    req->ccws[1].cmd = X3390_CMD_SEARCH;
    CSS_SET_ADDR(&req->ccws[1], &seek_ptr.cyl);
    req->ccws[1].flags = CSS_CCW_CC;
    req->ccws[1].length = 5;

    req->ccws[2].cmd = CSS_CMD_TIC;
    CSS_SET_ADDR(&req->ccws[2], &req->ccws[1]);
    req->ccws[2].flags = 0x00;
    req->ccws[2].length = 0;

    req->ccws[3].cmd = X3390_CMD_LD;
    CSS_SET_ADDR(&req->ccws[3], buf);
    req->ccws[3].flags = CSS_CCW_SLI;
    req->ccws[3].length = (uint16_t)n;

    dev->orb.flags = 0x0080FF00;

    seek_ptr.block = 0;
    seek_ptr.cyl = fdscb->cyl;
    seek_ptr.head = fdscb->head;
    seek_ptr.record = fdscb->rec;

    CssSendRequest(req);
    if(CssPerformRequest(req) != 0) {
        return -1;
    }
    CssDestroyRequest(req);
    return (int)n - (int)dev->irb.scsw.count;
no_op:
    KeDebugPrint("x3390: Not operational - drive was unplugged?\r\n");
    return -1;
}

