static int DevEnableTerm(struct css_device *dev)
{
    struct css_request *req;
    req = CssNewRequest(dev, 1);
    req->ccws[0].cmd = 0x27;
    CSS_SET_ADDR(&req->ccws[0], 0);
    req->ccws[0].flags = 0;
    req->ccws[0].length = 0;

    dev->orb.flags = 0x0080FF00;
    req->flags = CSS_REQUEST_MODIFY | CSS_REQUEST_IGNORE_CC;

    CssSendRequest(req);
    CssPerformRequest(req);
    CssDestroyRequest(req);
    return 0;
}

static int DevWriteTerm(struct css_device *dev, const void *buf, size_t n)
{
    struct css_request *write_req;
    int r;
    
    write_req = CssNewRequest(dev, 1);
    write_req->flags = CSS_REQUEST_MODIFY;

    /* TODO: We're overwriting read-only memory, this is not good */
    for(r = 0; r < (int)n; r++) {
        if(((char *)buf)[r] == '\r') {
            ((char *)buf)[r] = 0x0a;
            ((char *)buf)[++r] = 0x0d;
        }
    }

    write_req->ccws[0].cmd = CSS_CMD_WRITE;
    CSS_SET_ADDR(&write_req->ccws[0], buf);
    write_req->ccws[0].flags = 0;
    write_req->ccws[0].length = (uint16_t)n;

    dev->orb.flags = 0x0080FF00;
	
    CssSendRequest(write_req);
    r = CssPerformRequest(write_req);
    CssDestroyRequest(write_req);
    return r;
}

static int DevReadTerm(struct css_device *dev, void *buf, size_t n)
{
    struct css_request *read_req;
    int r;
    
    read_req = CssNewRequest(dev, 1);
    read_req->flags = CSS_REQUEST_MODIFY | CSS_REQUEST_IGNORE_CC | CSS_REQUEST_WAIT_ATTENTION;

    /* Using the generic CSS_CMD_READ is not valid, a special version
     **must** be used - otherwise it won't work */
    read_req->ccws[0].cmd = 0x0A;
    CSS_SET_ADDR(&read_req->ccws[0], buf);
    read_req->ccws[0].flags = 0x20;
    read_req->ccws[0].length = (uint16_t)n;

    dev->orb.flags = 0x0080FF00;

    CssSendRequest(read_req);
    r = CssPerformRequest(read_req);
    CssDestroyRequest(read_req);
    return r;
}

